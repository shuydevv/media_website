<?php

namespace App\Support;

class Text
{
    /**
     * Нормализует текст перед рендером через white-space:pre-wrap: убирает
     * пробелы/табы в конце строк, схлопывает 3+ пустых строк подряд до одной,
     * обрезает пустые строки/пробелы по краям. Без этого лишние переносы и
     * пробелы, случайно оставшиеся в тексте при вводе (особенно при
     * копипасте в админке), рендерятся буквально и дают непредсказуемые
     * отступы — pre-wrap сохраняет whitespace как есть, ничего не схлопывает
     * сам по себе, в отличие от обычного HTML.
     */
    public static function normalize(?string $s): ?string
    {
        if ($s === null) {
            return null;
        }

        $s = str_replace(["\r\n", "\r"], "\n", $s);
        $s = preg_replace('/[ \t]+$/m', '', $s);
        $s = preg_replace('/\n{3,}/', "\n\n", $s);
        $s = trim($s, " \t\n\r\0\x0B");

        return $s === '' ? null : $s;
    }
}
